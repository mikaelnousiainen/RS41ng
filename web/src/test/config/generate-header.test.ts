describe("generateConfigH — enum_defines", () => {
  it("emits enum constant #defines before the field that uses them", () => {
    const schema: Schema = {
      hardware: {
        _section: "Hardware",
        _tier: "simple",
        _order: 0,
        type: {
          define: "HW",
          type: "enum",
          label: "HW",
          tier: "simple",
          default: "RS41",
          define_style: "toggle",
        },
      },
      gps: {
        _section: "GPS",
        _tier: "advanced",
        _order: 1,
        dynamic_model: {
          define: "GPS_DYNAMIC_MODEL",
          type: "enum_constant",
          label: "Dynamic Model",
          tier: "advanced",
          default: 6,
          enum_defines: [
            { define: "GPS_DYNAMIC_MODEL_AIRBORNE_1G", value: 6 },
            { define: "GPS_DYNAMIC_MODEL_PORTABLE", value: 0 },
          ],
          values: [
            { value: 0, label: "Portable" },
            { value: 6, label: "Airborne <1g" },
          ],
        },
      },
    } as unknown as Schema;

    const out = generateConfigH(schema, {});
    const posConst = out.indexOf("#define GPS_DYNAMIC_MODEL_AIRBORNE_1G");
    // Match the field define with its value to avoid matching the enum_define prefix
    const posField = out.indexOf("#define GPS_DYNAMIC_MODEL 6");
    expect(posConst).toBeGreaterThanOrEqual(0);
    expect(posField).toBeGreaterThan(posConst);
    expect(out).toContain("#define GPS_DYNAMIC_MODEL_AIRBORNE_1G 6");
    expect(out).toContain("#define GPS_DYNAMIC_MODEL_PORTABLE 0");
  });
});

describe("generateConfigH — compile-time guards", () => {
  it("emits GPS NMEA / I2C conflict guard after gps section", () => {
    const out = generateConfigH(makeSchema(), {});
    expect(out).toContain(
      "#if (GPS_NMEA_OUTPUT_VIA_SERIAL_PORT_ENABLE) && ((RADIO_SI5351_ENABLE) || (SENSOR_BMP280_ENABLE))"
    );
  });

  it("emits pulse counter conflict guard after sensors section", () => {
    const out = generateConfigH(makeSchema(), {});
    expect(out).toContain(
      "#if (PULSE_COUNTER_ENABLE) && ((GPS_NMEA_OUTPUT_VIA_SERIAL_PORT_ENABLE) || (RADIO_SI5351_ENABLE) || (SENSOR_BMP280_ENABLE))"
    );
  });
});
